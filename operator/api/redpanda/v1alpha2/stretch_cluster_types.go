// StretchLogging defines log level settings for stretch clusters.
//
// Forked from Logging: uses StretchUsageStats instead of UsageStats.
type StretchLogging struct {
	// Specifies the log level.
	LogLevel *string `json:"logLevel,omitempty"`
	// Defines settings for usage statistics reporting.
	UsageStats *StretchUsageStats `json:"usageStats,omitempty"`
}

// StretchUsageStats defines usage statistics settings for stretch clusters.
//
// Forked from UsageStats: removed deprecated Organization field and unused ClusterID field.
type StretchUsageStats struct {
	// Specifies whether usage statistics are enabled.
	Enabled *bool `json:"enabled,omitempty"`
}

// StretchListeners configures listener settings for stretch clusters.
//
// Forked from Listeners: uses StretchAPIListener (collapsed from Admin, HTTP,
// Kafka, SchemaRegistry — all identical after removing deprecated KafkaEndpoint
// from HTTP and SchemaRegistry) and StretchRPC.
type StretchListeners struct {
	// Configures settings for the Admin API listeners.
	Admin *StretchAPIListener `json:"admin,omitempty"`
	// Configures settings for the HTTP Proxy listeners.
	HTTP *StretchAPIListener `json:"http,omitempty"`
	// Configures settings for the Kafka API listeners.
	Kafka *StretchAPIListener `json:"kafka,omitempty"`
	// Configures settings for the RPC API listener.
	RPC *StretchRPC `json:"rpc,omitempty"`
	// Configures settings for the Schema Registry listeners.
	SchemaRegistry *StretchAPIListener `json:"schemaRegistry,omitempty"`
}

// StretchListenerTLS configures TLS for a listener in stretch clusters.
//
// Forked from ListenerTLS: removed deprecated SecretRef field (had no effect).
type StretchListenerTLS struct {
	// References a specific certificate for the listener.
	Cert *string `json:"cert,omitempty"`
	// Specifies whether TLS is enabled for the listener.
	Enabled *bool `json:"enabled,omitempty"`
	// Indicates whether client authentication (mTLS) is required.
	RequireClientAuth *bool `json:"requireClientAuth,omitempty"`
	// TrustStore allows setting the `truststore_path` on this listener.
	TrustStore *TrustStore `json:"trustStore,omitempty"`
}

// StretchListener configures a single listener in stretch clusters.
//
// Forked from Listener: references StretchListenerTLS instead of ListenerTLS.
type StretchListener struct {
	// Specifies whether this Listener is enabled.
	Enabled *bool `json:"enabled,omitempty"`
	// Specifies the authentication method for this listener.
	AuthenticationMethod *string `json:"authenticationMethod,omitempty"`
	AppProtocol          *string `json:"appProtocol,omitempty"`
	// Specifies the container port number for this listener.
	Port *int32 `json:"port,omitempty"`
	// Configures TLS settings for the internal listener.
	TLS *StretchListenerTLS `json:"tls,omitempty"`
	// Specifies the template used for generating the advertised addresses of Services.
	PrefixTemplate *string `json:"prefixTemplate,omitempty"`
}

// StretchExternalListener configures an external listener in stretch clusters.
//
// Forked from ExternalListener: embeds StretchListener instead of Listener.
type StretchExternalListener struct {
	StretchListener `json:",inline"`

	// Specifies the network port that the external Service listens on.
	AdvertisedPorts []int32 `json:"advertisedPorts,omitempty"`
	NodePort        *int32  `json:"nodePort,omitempty"`
}

// StretchRPC configures the RPC API listener for stretch clusters.
//
// Forked from RPC: references StretchListenerTLS instead of ListenerTLS.
type StretchRPC struct {
	// Specifies the container port number for the internal listener.
	Port *int `json:"port,omitempty"`
	// Configures TLS settings for the internal listener.
	TLS *StretchListenerTLS `json:"tls,omitempty"`
}

// StretchAPIListener configures an API listener (Admin, HTTP, Kafka, SchemaRegistry)
// for stretch clusters.
//
// Collapsed from Admin, HTTP, Kafka, SchemaRegistry — all identical after removing
// deprecated KafkaEndpoint field from HTTP and SchemaRegistry (was not respected)
// and deprecated SecretRef from ListenerTLS (had no effect).
type StretchAPIListener struct {
	StretchListener `json:",inline"`

	// Defines settings for the external listeners.
	External map[string]*StretchExternalListener `json:"external,omitempty"`
}
