// StretchResources defines container resource settings for stretch clusters.
//
// Forked from Resources: changed Limits/Requests from *map[corev1.ResourceName]resource.Quantity
// (pointer-to-map) to corev1.ResourceList (standard K8s type alias) for idiomatic usage.
type StretchResources struct {
	// Standard K8s resource limits.
	Limits corev1.ResourceList `json:"limits,omitempty"`
	// Standard K8s resource requests.
	Requests corev1.ResourceList `json:"requests,omitempty"`

	// Specifies the number of CPU cores.
	CPU *CPU `json:"cpu,omitempty"`
	// Specifies the amount of memory.
	Memory *Memory `json:"memory,omitempty"`
}

// StretchTuning defines autotuner settings for stretch clusters.
//
// Forked from Tuning: removed ExtraVolumeMounts and Resources fields which are
// init-container concerns (used by Statefulset.InitContainers.Tuning in the Helm chart)
// and have no meaning as cluster-level tuning configuration.
type StretchTuning struct {
	// Specifies the file path for ballast file.
	BallastFilePath *string `json:"ballast_file_path,omitempty"`
	// Defines the size of the ballast file.
	BallastFileSize *string `json:"ballast_file_size,omitempty"`
	// Specifies whether to increase the number of allowed asynchronous IO events.
	TuneAioEvents *bool `json:"tune_aio_events,omitempty"`
	// Specifies whether to create the ballast file.
	TuneBallastFile *bool `json:"tune_ballast_file,omitempty"`
	// Specifies whether to synchronize NTP.
	TuneClockSource *bool `json:"tune_clocksource,omitempty"`
	// Specifies the vendor, VM type, and storage device type that Redpanda runs on, in the format <vendor>:<vm>:<storage>.
	WellKnownIo *string `json:"well_known_io,omitempty"`
}

// StretchStorage configures storage for stretch clusters.
//
// Forked from Storage: uses StretchTiered instead of Tiered to pick up
// the cleaned-up StretchTieredConfig.
type StretchStorage struct {
	// Specifies the absolute path on the worker node to store the Redpanda data directory.
	HostPath *string `json:"hostPath,omitempty"`
	// Configures a PersistentVolumeClaim (PVC) template to create for each Pod.
	PersistentVolume *PersistentVolume `json:"persistentVolume,omitempty"`
	// Configures storage for the Tiered Storage cache.
	Tiered *StretchTiered `json:"tiered,omitempty"`
}

// StretchTiered configures tiered storage for stretch clusters.
//
// Forked from Tiered: uses StretchTieredConfig instead of TieredConfig.
type StretchTiered struct {
	// mountType: "none", "hostPath", "emptyDir", or "persistentVolume".
	MountType *string `json:"mountType,omitempty"`
	// Specifies the absolute path on the worker node to store the Tiered Storage cache.
	HostPath *string `json:"hostPath,omitempty"`
	// Configures a PersistentVolumeClaim (PVC) template for the Tiered Storage cache.
	PersistentVolume *PersistentVolume `json:"persistentVolume,omitempty"`
	// Configures Tiered Storage properties.
	Config *StretchTieredConfig `json:"config,omitempty"`
	// CredentialsSecretRef sets cloud_storage_secret_key and/or cloud_storage_access_key from a referenced Secret.
	CredentialsSecretRef *CredentialSecretRef `json:"credentialsSecretRef,omitempty"`
}

